<script lang="ts">
export default {
  data() {
    return {
      headers: [
        { active: true, key: 'exp_cnt', value: 'Y', description: '경험치 포함 조회 여부' },
        { active: false, key: 'max_cnt', value: 10, description: '조회 리스트 개수 최대값' },
        {},
      ],
      isHighLight: [] as boolean[],
    }
  },
  mounted() {
    this.headers.forEach(() => {
      this.isHighLight.push(false)
    })
  },
  methods: {
    rowHighLight(row: number) {
      this.isHighLight[row] = true
    },
    clearHighLight(row: number) {
      this.isHighLight[row] = false
    },
  },
}
</script>

<template>
  <div class="p-3" select-none>
    <p>Headers</p>
    <table name="headersTable" class="w-full text-left" select-none>
      <colgroup>
        <col class="act">
        <col class="key">
        <col class="val">
        <col class="desc">
      </colgroup>
      <thead>
        <tr>
          <th>Active</th>
          <th>Key</th>
          <th>Value</th>
          <th>Description</th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="(head, index) in headers" :key="index" :class="{ tableHighLite: isHighLight[index] }">
          <td>
            <span v-if="head.active">✅</span>
            <span v-else-if="head.active == null" />
            <span v-else>⬜</span>
          </td>
          <td>
            <input v-model="head.key" placeholder="Key" h-full w-full @focus="rowHighLight(index)" @blur="clearHighLight(index)">
          </td>
          <td>
            <input v-model="head.value" placeholder="Value" h-full w-full @focus="rowHighLight(index)" @blur="clearHighLight(index)">
          </td>
          <td>
            <input v-model="head.description" placeholder="Description" h-full w-full @focus="rowHighLight(index)" @blur="clearHighLight(index)">
          </td>
        </tr>
      </tbody>
    </table>
  </div>
</template>

